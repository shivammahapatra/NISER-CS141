#include <iostream>
using namespace std;
int main()
{
int n;
cout<<"Enter a week day number\n";
cin>>n;
if(n==1)
cout<<"SUNDAY";
else if(n==2)
cout<<"MONDAY";
else if(n==3)
cout<<"TUESDAY";
else if(n==4)
cout<<"WEDNESDAY";
else if(n==5)
cout<<"THURSDAY";
else if(n==6)
cout<<"FRIDAY";
else if(n==7)
cout<<"SATURDAY";
else
cout<<"Enter a valid input";
}

